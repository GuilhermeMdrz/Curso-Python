n = input('digite algo:')
print('O tipo desse valor é \033[1;31m{}\033[m'.format(type(n)))
print('Só tem espaços?\033[1;32m{}\033[m'.format(n.isspace()))
print('É um número?\033[1;33m{}\033[m'.format(n.isnumeric()))
print('É alfabético?\033[1;34m{}\033[m'.format(n.isalpha()))
print('É alfanumérico?\033[1;35m{}\033[m'.format(n.isalnum()))
print('Está em maiúsculas?\033[1;36m{}\033[m'.format(n.isupper()))
print('Está em minúsculas?\033[1;37m{}\033[m'.format(n.islower()))
print('Está capitalizada?\033[1;38m{}\033[m'.format(n.istitle()))
